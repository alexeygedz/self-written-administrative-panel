<h1>О проекте</h1>
<p>Резюме проекта - это самая важная часть бизнес-документа; другие люди прочитают резюме в первую очередь (а порой это и вовсе единственное, что они прочитают о вашем проекте), а вот писать его нужно в последнюю очередь. Резюме представляет собой краткую выжимку бизнес-документа, из которой должно сразу становиться ясно, какие именно действия потребуется предпринять.</p>
