<p>Резюме проекта - это не реферат, в котором дается краткий обзор и описывается направление проекта. Резюме - это больше “суть” проекта. Рефераты чаще пишутся в учебных заведениях, тогда как резюме проекта, как правило, служат деловым целям.</p>

